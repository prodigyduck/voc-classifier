def insert_sample_data(num_samples=10000):
    config = load_config()
    db_config = config["database"]

    conn = psycopg2.connect(
        host=db_config["host"],
        port=db_config["port"],
        dbname=db_config["name"],
        user=db_config["user"],
        password=db_config["password"]
    )
    cursor = conn.cursor()

    category_id_map = get_category_id_map(cursor)

    samples = generate_voc_samples(num_samples)

    insert_query = """
        INSERT INTO voc_classifier.vocs (title, content, category_id, priority, status, submitted_by, ui_related, created_at, updated_at)
        VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s)
    """

    batch_size = 1000
    for i in range(0, len(samples), batch_size):
        batch = samples[i:i + batch_size]
        batch_data = []
        for sample in batch:
            batch_data.append((
                sample["title"],
                sample["content"],
                category_id_map[sample["category"]],
                sample["priority"],
                sample["status"],
                sample["submitted_by"],
                sample["ui_related"],
                sample["created_at"],
                sample["created_at"]
            ))

        cursor.executemany(insert_query, batch_data)
        conn.commit()
        print(f"Inserted {min(i + batch_size, len(samples))} / {num_samples} records")

    cursor.close()
    conn.close()
    print(f"\n총 {num_samples}건의 샘플 데이터가 삽입되었습니다.")


if __name__ == "__main__":
    insert_sample_data(10000)
